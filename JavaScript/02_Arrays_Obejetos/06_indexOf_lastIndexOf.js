//IndexOf e LastIndexOf
//IndexOf e LastIndexOf são métodos de Array que retornam a posição de um elemento no array.
//O método indexOf retorna a primeira posição do elemento no array.
//O método lastIndexOf retorna a última posição do elemento no array.

const array = ['Banana', 'Maça', 'Pera', 'Uva', 'Melancia'];
console.log(array.indexOf('Banana')); //0
console.log(array.lastIndexOf('Uva')); //3