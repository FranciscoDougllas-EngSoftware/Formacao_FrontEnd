//Tipos de Dados

//String
const nome = 'Francisco';
console.log(typeof nome, nome);

//Number
const idade = 30;
console.log(typeof idade, idade);

//Boolean
const casado = true;
console.log(typeof casado, casado);

//Null
const nulo = null;
console.log(typeof nulo, nulo);

//Undefined
const  naoDefinido = undefined;
console.log(typeof naoDefinido, naoDefinido);

//Symbol
const simbolo = Symbol();
console.log(typeof simbolo, simbolo);


